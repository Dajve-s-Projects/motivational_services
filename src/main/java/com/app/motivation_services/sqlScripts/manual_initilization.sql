-- Drop all tables
DROP TABLE IF EXISTS author, book, foodstat, note, emotion;

-- Add all tables
CREATE TABLE Author
(
    id         BIGINT PRIMARY KEY AUTO_INCREMENT,
    authorName VARCHAR(255) NOT NULL
);

